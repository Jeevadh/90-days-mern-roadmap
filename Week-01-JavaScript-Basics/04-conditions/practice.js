// Write an if condition to check that age is between 14 and 90 inclusively.

// “Inclusively” means that age can reach the edges 14 or 90.

let personAges = {
  person1: 17,
  person2: 98,
};

if (personAges.person1 >= 14 && personAges.person1 <= 90) {
  console.log(`Person 1 is between 14 and 90.`);
} else {
  console.log(`Person 1 is not between 14 and 90.`);
}   

if (personAges.person2 >= 14 && personAges.person2 <= 90) {
  console.log(`Person 2 is between 14 and 90.`);
} else {
  console.log(`Person 2 is not between 14 and 90.`);
}




